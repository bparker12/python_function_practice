def run(name):
    for kid in name:
     print(f'{kid} ran like a fool')

def swing(name):
    for kid in name:
        print(f'{kid} swings real high')

def slide(name):
    for kid in name:
        print(f'{kid} slides down the slide')

def hide_and_seek(name):
    for kid in name:
        print(f'{kid} plays hide and seek')


running_kids = ["Pam", "Sam", "Andrea", "Will"]
swinging_kids = ["Marybeth", "Jenna", "Kevin", "Courtney"]
sliding_kids = ["Mike", "Jack", "Jennifer", "Earl"]
hiding_kids = ["Henry", "Heather", "Hayley", "Hugh"]


run(running_kids)
swing(swinging_kids)
slide(sliding_kids)
hide_and_seek(hiding_kids)


